
public class Level 
{
    public int number;
    public float time;
    public int minReward;
    public int maxReward;

    public Level(int number, float time, int minReward, int maxReward)
    {
        this.number = number;
        this.time = time;
        this.minReward = minReward;
        this.maxReward = maxReward;
    }
}
